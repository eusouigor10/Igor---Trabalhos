    private void botaoOk2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOk2ActionPerformed
 
        vetorForcasI[i] = Integer.parseInt(entradaForcaI.getText());
        vetorForcasJ[i] = Integer.parseInt(entradaForcaJ.getText());
        numeroForcas++;
        if(numeroForcas <= numeroEntradas){
        numeroForca.setText(String.valueOf(numeroForcas));
        }
        i++;
        
        if(i == numeroEntradas){
            botaoOk2.setVisible(false); 
        }
    }//GEN-LAST:event_botaoOk2ActionPerformed

    private void botaoOk3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOk3ActionPerformed
        resultanteI = 0;
        resultanteJ = 0;
        
        for(int j = 0; j < numeroEntradas; j++){
            resultanteI += vetorForcasI[j];
            resultanteJ += vetorForcasJ[j];
        }
        
        forcaResultanteI.setText(String.valueOf(resultanteI));
        forcaResultanteJ.setText(String.valueOf(resultanteJ));
    }//GEN-LAST:event_botaoOk3ActionPerformed

    private void botaoOk4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOk4ActionPerformed
        resultanteEixos = pow(resultanteI, 2) + pow(resultanteJ, 2);
        resultanteEixos = sqrt(resultanteEixos);
        resultanteDosEixos.setText(String.valueOf(resultanteEixos));
    }//GEN-LAST:event_botaoOk4ActionPerformed

