    private void botaoOk1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOk1ActionPerformed
        numeroEntradas = Integer.parseInt(entradaDeForcas.getText());
        if(numeroEntradas < 1 || numeroEntradas > 7){
            JOptionPane.showMessageDialog(this, "Quantidade inválida de entradas", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        numeroForca.setText(String.valueOf(numeroForcas));
        vetorForcasI = new int[numeroEntradas];
        vetorForcasJ = new int[numeroEntradas];
    }//GEN-LAST:event_botaoOk1ActionPerformed

